// different way to use exponents
// long way
console.log(Math.pow(4, 2));
console.log(Math.pow(2, 3));

// short way
console.log(4**2);
console.log(2**3);

// rounding down numbers
// using Math.floor()
console.log(Math.floor(5.75));

// using ~~ shorter way
console.log(~~5.75);

// converting string to number
// long way
const number1 = parseInt("1199")
console.log(number1)

// short way
const number2 = +"2199"
console.log(number2)