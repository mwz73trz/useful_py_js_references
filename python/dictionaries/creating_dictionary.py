# creating and using basic dictionary

dictionary = {
    "month": "june",
    "day": "16th",
    "year": "1973"
}

print(dictionary)

# retirieve value based on key

print(dictionary["month"])

# get() funtion to retrieve key

print(dictionary.get("month"))

# keys() function to retrieve all keys

print(dictionary.keys())

# values() retirieve all values

print(dictionary.values())